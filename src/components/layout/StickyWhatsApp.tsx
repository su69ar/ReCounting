"use client";

import { useRef, useState } from "react";
import { gsap, useGSAP, ScrollTrigger } from "@/lib/gsap";
import { prefersReducedMotion } from "@/lib/motion";
import { siteConfig } from "@/lib/site";

export function StickyWhatsApp() {
  const buttonRef = useRef<HTMLAnchorElement>(null);
  const tooltipRef = useRef<HTMLDivElement>(null);
  const [isHovered, setIsHovered] = useState(false);
  const [isVisible, setIsVisible] = useState(false);

  useGSAP(() => {
    if (!buttonRef.current || prefersReducedMotion()) {
      setIsVisible(true);
      return;
    }

    ScrollTrigger.create({
      start: "top -400",
      onUpdate: (self) => {
        setIsVisible(self.progress > 0);
      },
    });

    gsap.to(buttonRef.current, {
      scale: 1.05,
      duration: 1.5,
      ease: "sine.inOut",
      repeat: -1,
      yoyo: true,
    });
  }, { scope: buttonRef });

  useGSAP(() => {
    if (!buttonRef.current || prefersReducedMotion()) return;

    if (isHovered) {
      gsap.to(buttonRef.current, {
        scale: 1.1,
        rotation: 5,
        duration: 0.3,
        ease: "back.out(2)",
      });
      gsap.to(tooltipRef.current, {
        autoAlpha: 1,
        x: 0,
        duration: 0.3,
        ease: "power2.out",
      });
    } else {
      gsap.to(buttonRef.current, {
        scale: 1,
        rotation: 0,
        duration: 0.3,
        ease: "power2.out",
      });
      gsap.to(tooltipRef.current, {
        autoAlpha: 0,
        x: 10,
        duration: 0.2,
        ease: "power2.in",
      });
    }
  }, [isHovered]);

  const whatsappUrl = `https://wa.me/${siteConfig.phoneIntl.replace(/\D/g, "")}?text=Hi%20ReCounting%2C%20I%27d%20like%20to%20know%20more%20about%20your%20services.`;

  return (
    <div
      className={`fixed bottom-6 right-6 z-40 transition-opacity duration-300 ${
        isVisible ? "opacity-100" : "opacity-0 pointer-events-none"
      }`}
    >
      <div
        ref={tooltipRef}
        className="absolute right-full mr-3 top-1/2 -translate-y-1/2 
                   bg-neutral-900 text-white text-sm px-4 py-2 rounded-lg 
                   whitespace-nowrap opacity-0"
      >
        Chat with us on WhatsApp
        <span className="absolute right-0 top-1/2 -translate-y-1/2 translate-x-1/2
                       border-8 border-transparent border-l-neutral-900" />
      </div>

      <a
        ref={buttonRef}
        href={whatsappUrl}
        target="_blank"
        rel="noreferrer"
        className="flex h-14 w-14 items-center justify-center rounded-full 
                   bg-[#25D366] text-white shadow-lg shadow-[#25D366]/30
                   hover:shadow-xl hover:shadow-[#25D366]/40 transition-shadow"
        onMouseEnter={() => setIsHovered(true)}
        onMouseLeave={() => setIsHovered(false)}
        aria-label="Contact us on WhatsApp"
      >
        <svg className="h-7 w-7" fill="currentColor" viewBox="0 0 24 24">
          <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
        </svg>
      </a>
    </div>
  );
}
